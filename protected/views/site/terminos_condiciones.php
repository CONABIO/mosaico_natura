		  <h4>MOTIVOS DE DESCALIFICACI&Oacute;N</h4>
                      <p>Los motivos  de descalificación inmediata del participante son los siguientes:</p>
                      <ul>
                        <li><p>Fotografías, en lo sucesivo denominado el &ldquo;material&rdquo;, que no cumplan con  cualquiera de los requisitos de participación.</p></li>
                        <li><p>Incurrir en cualquier conducta que implique maltrato a los sujetos de la  fotografía</p></li>
                        <li><p>Incurrir en descrédito, difamación o calumnias en contra del &ldquo;Comité  Organizador&rdquo; o de los miembros del jurado de este concurso.</p></li>
                        <li><p>Presentación de material ajeno a su autoría y que se demuestre que es  creación de un tercero.</p></li>
                        <li><p>No poder ceder los derechos de uso no exclusivo sobre el material.</p></li>
                        <li><p>Fotografías manipuladas digitalmente quitando o añadiendo objetos ajenos  a la imagen original por medio de algún programa para edición de imágenes.</p></li>
                        <li><p>Fotografías que hayan ganado otro concurso; que formen parte de  cualquier otra colección pública o privada; o que hayan sido utilizada  previamente con fines comerciales.</p></li>
                      </ul>
			          <p>El &ldquo;Comité  Organizador&rdquo; puede solicitar el original de cualquiera de las obras  fotográficas si así lo requiere o considera, de forma discrecional. El &ldquo;Comité  Organizador&rdquo; determinará el plazo para la entrega de dicho original y lo hará  del conocimiento del participante. En caso de no entregarlo, esto podrá ser  motivo de descalificación.</p>
