			<p>
				El Comit&eacute; Organizador conformado por la Comisi&oacute;n
				Nacional para el Conocimiento y Uso de la Biodiversidad (CONABIO),
				National Geographic en Espa&ntilde;ol, Nikon de M&eacute;xico,
				Mexican&iacute;simo, la Secretar&iacute;a del Medio Ambiente del
				Gobierno del Distrito Federal (SEDEMA), Ibero 90.9, Fundaci&oacute;n
				ACIR, Secretar&iacute;a de Turismo (SECTUR), RECREA y la
				Comisi&oacute;n Nacional de &Aacute;reas Naturales Protegidas
				(CONANP) convoca al <strong>&ldquo;Segundo Concurso
					Nacional de Fotograf&iacute;a de la Naturaleza&rdquo;</strong> en el cual
				podr&aacute;n participar todas las personas, nacionales o
				extranjeras radicadas en M&eacute;xico, aficionados o profesionales
				de la fotograf&iacute;a.
			</p>
			<p>
				La(s) fotograf&iacute;a(s) y video (s) debe(n) representar la <strong>riqueza
					natural en M&eacute;xico, </strong>es decir especies de plantas, hongos y
				animales nativos del pa&iacute;s en vida silvestre, ecosistemas,
				usos de la biodiversidad, naturaleza en destinos tur&iacute;sticos y
				procesos o actividades que degradan la naturaleza. No se aceptan
				fotograf&iacute;as o videos que previamente hayan ganado en otro
				concurso.
			</p>
			<p>
				De las fotograf&iacute;as recibidas, el jurado seleccionar&aacute; *<strong>28</strong>
				fotograf&iacute;as, <strong>5 de cada tema en la
					categor&iacute;a de adultos (25) y 3 para la de j&oacute;venes</strong>, que
				formar&aacute;n parte de la exposici&oacute;n &ldquo;Mosaico Natura
				M&eacute;xico&rdquo; a exponerse en las rejas de la Av. Reforma en
				el Bosque de Chapultepec, Distrito Federal y de una
				exposici&oacute;n itinerante en la rep&uacute;blica mexicana
			</p>
			<p>
				<strong>Nota:</strong> Los primeros lugares de cada categor&iacute;a
				seleccionados en el <strong>Primer Concurso de
					Fotograf&iacute;a de Naturaleza Mosaico Natura M&eacute;xico</strong> no
				podr&aacute;n participar en este certamen. Su participaci&oacute;n
				se aceptar&aacute; nuevamente en el pr&oacute;ximo concurso.
			</p>
			<h4>
				<strong>CATEGOR&Iacute;AS</strong>
			</h4>
			<p>
				Fotograf&iacute;a Adultos de 18 a&ntilde;os en adelante<br>
				Fotograf&iacute;a J&oacute;venes menores de 18 a&ntilde;os<br> Categor&iacute;a de video
			</p>
			<p>
				<strong>ADULTOS</strong><br> Cada participante puede enviar
				hasta un l&iacute;mite de <strong>5 fotograf&iacute;as, 1
					por tema,</strong> para ser incluidas en los siguientes temas:
			</p>
			<p>
				<strong>Fauna:</strong> Se podr&aacute;n tomar toda clase de
				animales en vida silvestre, desde peque&ntilde;os insectos hasta
				grandes mam&iacute;feros, siempre que &eacute;stos, no est&eacute;n
				en cautiverio o sean animales dom&eacute;sticos y/o mascotas.
			</p>
			<p>
				<strong>Plantas y hongos: </strong>Se podr&aacute;n tomar toda clase
				de plantas y hongos en vida silvestre, No se aceptar&aacute;n
				fotograf&iacute;as de plantas cultivadas de maceta o jard&iacute;n.
			</p>
			<p>
				<strong>Naturaleza y ser humano:</strong> Usos y costumbres
				relacionados con el entorno natural (actividades agr&iacute;colas,
				artesanales, gastron&oacute;micas, medicinales, espirituales,
				recreativas, ceremoniales). Escenas de manejo sustentable y
				relaci&oacute;n positiva entre humano y naturaleza.
			</p>
			<p>
				<strong>Paisajes con ecosistemas: </strong>Vistas a&eacute;reas,
				terrestres, acu&aacute;ticas y subacu&aacute;ticas, donde se
				enfatiza los elementos de la naturaleza.
			</p>
			<p>
				<strong>Solo un mundo: </strong>Im&aacute;genes de paisajes, flora y
				fauna deteriorados o impactados directa o indirectamente por
				actividades humanas.
			</p>
			<p>&nbsp;</p>
			<p>
				<strong>J&Oacute;VENES - menores de 18 a&ntilde;os</strong><br>
				Esta categor&iacute;a est&aacute; dedicada a j&oacute;venes talentos
				de la fotograf&iacute;a de 17 a&ntilde;os de edad y menores. Tiene
				como finalidad incentivar a los j&oacute;venes a tomar conciencia de
				su entorno a trav&eacute;s de la fotograf&iacute;a de naturaleza.
			</p>
			<p>
				Podr&aacute;n participar todos los j&oacute;venes nacionales o
				extranjeros radicados en M&eacute;xico, menores de 18 a&ntilde;os.
				Cada participante puede enviar hasta <strong>2</strong>
				fotograf&iacute;as en este tema.
			</p>
			<p>
				<strong>Tema libre j&oacute;venes menores de 18
					a&ntilde;os.</strong> Se puede enviar cualquier fotograf&iacute;a
				relacionada con la riqueza natural de M&eacute;xico, es decir,
				animales o plantas en su ambiente natural, paisajes naturales,
				actividades relacionadas con el hombre y la naturaleza. No se
				aceptar&aacute;n fotograf&iacute;as de plantas cultivadas, animales
				en cautiverio o cualquier otro ambiente controlado.
			</p>
			<p>&nbsp;</p>
			<p>
				<strong>VIDEO - Video al natural</strong><br> En esta
				categor&iacute;a se podr&aacute;n enviar videos que representen la
				belleza natural de alg&uacute;n lugar, los h&aacute;bitos
				alimenticios, reproductivos, de propagaci&oacute;n,
				interacci&oacute;n entre especies, de migraci&oacute;n,
				etc&eacute;tera; de alguna o varias especies de animales o plantas y
				su entorno en una narrativa visual.
			</p>
			<p>Se aceptar&aacute; el uso de cualquier programa para
				edici&oacute;n de videos con la finalidad de editar y musicalizar
				los videos. La musicalizaci&oacute;n, si es el caso, deber&aacute;
				ser m&uacute;sica original o bien pistas de audio libres de
				copyright o con derechos de distribuci&oacute;n sin fines de lucro.</p>
			<p>La categor&iacute;a de video, est&aacute; limitada a 1 video
				por participante.</p>
			<p>
				<strong>&nbsp;</strong>
			</p>
			<h4>&iquest;C&Oacute;MO PARTICIPAR?</h4>
			<p>
				2.1 El registro oficial de participaci&oacute;n deber&aacute; ser
				en: www.mosaiconatura.net, donde cada participante se
				registrar&aacute; por medio de un formulario b&aacute;sico, donde se
				solicitar&aacute; informaci&oacute;n b&aacute;sica tanto de
				fot&oacute;grafo como datos de la fotograf&iacute;a, es la
				&uacute;nica manera para poder ser aceptado como uno de los
				participantes. Todos los registros oficiales ser&aacute;n
				confirmados v&iacute;a correo electr&oacute;nico.<br> <br>
				2.2 Antes de registrarse, el participante deber&aacute; de haber
				le&iacute;do, entendido y aceptado las Bases del Concurso.
			</p>
			<p>2.3 &Uacute;nicamente se aceptar&aacute; el env&iacute;o
				electr&oacute;nico de fotograf&iacute;as en formato jpg, con
				dimensiones de 3,000 a 4,500 pixeles del lado m&aacute;s largo de la
				imagen y una resoluci&oacute;n de 300DPI&acute;s (pixeles por
				pulgada).</p>
			<p>2.4 Se solicita a todos los concursantes conservar
				&iacute;ntegras las im&aacute;genes sometidas al concurso (es decir,
				guardar la imagen original en las mismas condiciones, sin
				mutilaciones ni agregados, cambios de color, entre otros), ya que en
				caso de resultar ganadoras de alg&uacute;n premio o de ser
				finalistas, el &ldquo;Comit&eacute; Organizador&rdquo; podr&aacute;
				solicitarlas.</p>
			<p>2.5 Los videos deber&aacute;n enviarse en formato MPG-2 con
				dimensiones de 1920 x 1080 pixeles o bien Quicktime MOV con
				cualquiera de los siguiente codecs: ProRes, DVCPRO HD 1080 p30 o
				Photo JPG con dimensiones de 1920 x 1080 pixeles y duraci&oacute;n
				de 30 a 60 segundos. Los videos, no deber&aacute;n tener
				ning&uacute;n tipo de marca de agua ni subt&iacute;tulos.</p>
			<p>2.6 Para la categor&iacute;a juvenil el concursante
				deber&aacute; enviar la fotograf&iacute;a original tal cu&aacute;l
				sale de la c&aacute;mara, para evitar confusiones de dimensiones.</p>
			<p>&nbsp;</p>
			<p>Al momento de registrarse, el participante da al
				&ldquo;Comit&eacute; Organizador&rdquo;:</p>
			<p>
				- Su conformidad y aceptaci&oacute;n de los t&eacute;rminos y
				condiciones de las Bases. <br> - Su conformidad y
				aceptaci&oacute;n para que el jurado determine y resuelva de forma
				unilateral e inapelable aquellas disposiciones contempladas y no
				contempladas en las presentes Bases.<br> - Su aceptaci&oacute;n
				a la decisi&oacute;n del jurado, la cual es inapelable.<br> -
				Su conformidad y aceptaci&oacute;n para recibir por cualquier medio
				informaci&oacute;n relacionada con las actividades del concurso
				mencionado, pudiendo solicitar libremente el no env&iacute;o de
				dicha informaci&oacute;n en cualquier momento.<br> - Su
				conformidad y aceptaci&oacute;n para que el &ldquo;Comit&eacute;
				Organizador&rdquo; utilice las fotograf&iacute;as y videos para
				cualquier actividad de difusi&oacute;n cultural, expositiva o
				digital propias a las instituciones siempre y cuando sea sin fines
				de lucro.<br> Aceptado lo anterior, el participante reconoce
				que concede un licenciamiento de derechos de uso no exclusivos sobre
				las obras fotogr&aacute;ficas presentadas al concurso al <a
					href="http://www.biodiversidad.gob.mx/recursos/bancoimg.html"
					target="_blank">Banco de Im&aacute;genes</a> de CONABIO, de donde
				podr&aacute;n ser prestadas al p&uacute;blico sin fines de lucro e
				incluyendo el cr&eacute;dito correspondiente.
			</p>
			<p>- Categor&iacute;a Juvenil &ndash;</p>
			<p>&Uacute;nicamente se aceptar&aacute; el env&iacute;o
				electr&oacute;nico de fotograf&iacute;as en formato jpg, debiendo
				enviar los archivos tal cual salen de la c&aacute;mara. Los menores
				deber&aacute;n contar con el consentimiento de sus padres o tutores
				para poder participar en este concurso y se sujetar&aacute;n a los
				puntos 2.2, 2.4 y 2.5 de esta convocatoria.</p>
			<h4>&iquest;QU&Eacute; HAY QUE HACER PARA PARTICIPAR?</h4>
			<p>3.1 La(s) fotograf&iacute;a(s) debe(n) de representar la
				biodiversidad de M&eacute;xico, especies de plantas y animales en
				vida silvestre, el entorno en donde habitan, paisajes naturales o
				actividades que impactan a nuestro mundo. S&oacute;lo se
				aceptar&aacute;n fotograf&iacute;as tomadas a partir del a&ntilde;o
				2012 hasta la actualidad. El concursante podr&aacute; participar
				hasta con 5 (cinco) fotograf&iacute;as (1 por tema), ya sea en
				blanco y negro y/o color y 1 (un) video para la categor&iacute;a de
				video al natural.</p>
			<p>3.2 Las fotograf&iacute;as recibidas no podr&aacute;n tener
				ninguna firma, sello o alguna otra marca de identificaci&oacute;n.
				Las obras fotogr&aacute;ficas que hayan ganado previamente otros
				concursos de fotograf&iacute;a, que formen parte de cualquier otra
				colecci&oacute;n p&uacute;blica o privada o que ya hayan sido
				utilizadas con fines comerciales, no ser&aacute;n elegibles ni
				consideradas por el jurado.</p>
			<p>3.3 Los clips de video no deber&aacute;n contener marcas de
				agua ni subt&iacute;tulos y en caso de musicalizaci&oacute;n,
				deber&aacute; ser m&uacute;sica original o bien pistas sin copyright
				o con permisos de distribuci&oacute;n sin fines de lucro.</p>
			<p>3.4 Los concursantes que participen podr&aacute;n utilizar
				ajustes generales (exposici&oacute;n, saturaci&oacute;n, contraste,
				etc&eacute;tera). No se aceptar&aacute;n fotograf&iacute;as que
				hayan sido manipuladas digitalmente eliminando partes de la imagen o
				sustituirlas con otros elementos diferentes a la imagen original.</p>
			<p>
				<strong>&iquest;D&Oacute;NDE SE PUEDE CONCURSAR?</strong><br>&Uacute;nicamente
				en la p&aacute;gina oficial del concurso Mosaico Natura
				M&eacute;xico: <strong>www.mosaiconatura.net</strong>
			</p>
			<p>
				<strong>&iquest;HASTA CU&Aacute;NDO SE PUEDE PARTICIPAR?</strong><br>El
				periodo de registro y participaci&oacute;n ser&aacute; a partir de
				las 12:00 horas del lunes 7 de diciembre de 2015 hasta las 23:00
				horas el lunes 29 de febrero de 2016, que ser&aacute; la fecha y
				hora l&iacute;mite para registrar la(s) fotograf&iacute;a(s) y
				videos. La vigencia del concurso es del lunes 7 de diciembre de 2015
				y hasta la fecha de premiaci&oacute;n a los ganadores, misma que se
				celebrar&aacute; en el marco de la Sexta Semana de la Diversidad
				Biol&oacute;gica en mayo de 2016.
			</p>
			<h4>RESPONSABILIDADES</h4>
			<p>&ldquo;La CONABIO&rdquo; podr&aacute; conservar las
				fotograf&iacute;as y videos como parte de su acervo &ldquo;Banco de
				im&aacute;genes&rdquo; y podr&aacute;, con la aceptaci&oacute;n
				impl&iacute;cita del participante desde el registro de sus
				im&aacute;genes, utilizar dichas fotograf&iacute;as y videos para
				realizar actividades, en M&eacute;xico y el extranjero, de
				difusi&oacute;n cultural y expositiva, as&iacute; como actividades
				sin fines de lucro, debiendo otorgar, en este caso, los
				cr&eacute;ditos correspondientes al fot&oacute;grafo /
				vide&oacute;grafo reconociendo el participante que concede un
				licenciamiento de derechos de uso no exclusivos sobre las obras.</p>
			<h4>MOTIVOS DE DESCALIFICACI&Oacute;N</h4>
			<p>
				Son motivos de descalificaci&oacute;n inmediata los siguientes:<br>
				- En caso de que el participante que haya enviado la(s)
				fotograf&iacute;a(s) y/o videos, en lo sucesivo denominado el
				&ldquo;material&rdquo;, no cumpla(n) con cualquiera de los
				requisitos de participaci&oacute;n.<br> - En caso de que el
				participante incurra en cualquier conducta que implique maltrato a
				los sujetos de la fotograf&iacute;a y/o descr&eacute;dito,
				difamaci&oacute;n o calumnias en contra del &ldquo;Comit&eacute;
				Organizador&rdquo; o de los miembros del jurado de este concurso.<br>
				- En caso de que el participante haya presentado un material que no
				sea de su creaci&oacute;n y que se demuestre que es creaci&oacute;n
				de un tercero.<br> - En caso de que el participante no pueda
				ceder los derechos de uso no exclusivo sobre el material.<br> -
				Si las fotograf&iacute;as han sido manipuladas digitalmente quitando
				o a&ntilde;adiendo objetos ajenos a la imagen original por medio de
				alg&uacute;n programa para edici&oacute;n de im&aacute;genes.<br>
				- En caso que el participante haya ganado otro concurso con este
				mismo material; que &eacute;ste forme parte de cualquier otra
				colecci&oacute;n p&uacute;blica o privada; o haya sido utilizada
				previamente con fines comerciales.<br> - El
				&ldquo;Comit&eacute; Organizador&rdquo; puede solicitar el original
				de cualquiera de las obras fotogr&aacute;ficas si as&iacute; lo
				requiere o considera, de forma discrecional. El &ldquo;Comit&eacute;
				Organizador&rdquo; determinar&aacute; el plazo para la entrega de
				dicho original y lo har&aacute; del conocimiento del participante.
				En caso de no entregarlo, esto podr&aacute; ser motivo de
				descalificaci&oacute;n.
			</p>
